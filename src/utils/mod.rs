//! # 工具模块
//!
//! 本模块提供通用的工具函数和类型定义，供其他模块使用。
//!
//! ## 模块内容
//!
//! - [`tools`] - 通用工具函数集合
//! - [`types`] - 通用数据类型定义和结构体
//!
//! ## 工具函数示例
//!
//! ```no_run
//! use bupt_rs::utils::tools;
//!
//! // 二维码扫描功能
//! let qr_result = tools::scan_qrcode("/path/to/qrcode.jpg");
//! match qr_result {
//!     Ok(signin_info) => println!("扫描成功"),
//!     Err(e) => println!("扫描失败: {}", e),
//! }
//! ```
//!
//! ## 通用类型
//!
//! 本模块定义了在多个校园系统中共用的数据结构，如：
//!
//! - 课程信息结构体
//! - 作业信息结构体
//! - 响应数据包装类型
//! - 错误类型定义
//!
//! 这些类型确保了不同模块间数据交换的一致性和类型安全。

pub mod tools;
pub mod types;
